
package Tugas5;

public class Kendaraan {
    // atribut dengan akses privat
    private String noPolisi;
    private String merk;
    private int tahun;

    // method getter dan setter untuk setiap atribut privat tersebut.

    // Getter dan Setter untuk noPolisi
    public String getNoPolisi() {
        return this.noPolisi;
    }

    public void setNoPolisi(String noPolisi) {
        this.noPolisi = noPolisi;
    }

    // Getter dan Setter untuk merk
    public String getMerk() {
        return this.merk;
    }

    public void setMerk(String merk) {
        this.merk = merk;
    }

    // Getter dan Setter untuk tahun
    public int getTahun() {
        return this.tahun;
    }

    public void setTahun(int tahun) {
        this.tahun = tahun;
    }
}
    

